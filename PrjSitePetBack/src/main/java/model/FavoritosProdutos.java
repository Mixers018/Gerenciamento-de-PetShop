package model;

public class FavoritosProdutos {
	private int id;
	private int idProd;
	public FavoritosProdutos(int id, int idProd) {
		super();
		this.id = id;
		this.idProd = idProd;
	}
	public FavoritosProdutos() {
		super();
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getIdProd() {
		return idProd;
	}
	public void setIdProd(int idProd) {
		this.idProd = idProd;
	}
	
	
	
	
	
	
	
}
